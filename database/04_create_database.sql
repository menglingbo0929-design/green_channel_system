CREATE PROCEDURE batch_insert_approval_record(IN total INT)
BEGIN
    DECLARE i INT DEFAULT 1;
    DECLARE level_arr VARCHAR(255) DEFAULT 'STUDENT,COUNSELOR,COLLEGE,SCHOOL,CONFIRMATION,SYSTEM';
    DECLARE action_arr VARCHAR(255) DEFAULT 'SUBMIT,APPROVE,RETURN,REJECT,MODIFY,CANCEL';
    START TRANSACTION;
    WHILE i <= total DO
        INSERT INTO approval_record
        (`application_id`, `review_round`, `approval_level`, `approver_id`, `approver_name_snapshot`, `action`, `comment`, `old_status`, `new_status`, `modified_fields`, `request_id`)
        VALUES (
            FLOOR(RAND()*20)+1,
            FLOOR(RAND()*3)+1,
            SUBSTRING_INDEX(SUBSTRING_INDEX(level_arr, ',', FLOOR(RAND()*6)+1), ',', -1),
            IF(FLOOR(RAND()*10) = 0, NULL, FLOOR(RAND()*5000)+10000),
            CONCAT('测试用户',i),
            SUBSTRING_INDEX(SUBSTRING_INDEX(action_arr, ',', FLOOR(RAND()*6)+1), ',', -1),
            IF(FLOOR(RAND()*2)=0, CONCAT('自动测试审核意见',i), NULL),
            'DRAFT',
            CASE FLOOR(RAND()*6)
                WHEN 0 THEN 'COUNSELOR_AUDIT'
                WHEN 1 THEN 'COLLEGE_AUDIT'
                WHEN 2 THEN 'SCHOOL_AUDIT'
                WHEN 3 THEN 'TO_CONFIRM'
                WHEN 4 THEN 'CONFIRMED'
                ELSE 'DRAFT'
            END,
            IF(FLOOR(RAND()*3)=0, JSON_OBJECT('applyAmount',ROUND(RAND()*8000,2)), NULL),
            CONCAT('req_20260717_',LPAD(i,6,'0'))
        );
        SET i = i + 1;
    END WHILE;
    COMMIT;
END$$
